export const getStatesByRegion = (region: string) => {
  if (region === "all") return indianStates;
  return indianStates.filter((state) => state.region === region);
};

export const getAllPlaces = () => {
  return indianStates.flatMap((state) =>
    state.places.map((place) => ({
      ...place,
      stateName: state.name,
      region: state.region,
    }))
  );
};

export const searchPlaces = (query: string) => {
  const lowerQuery = query.toLowerCase();
  return getAllPlaces().filter(
    (place) =>
      place.name.toLowerCase().includes(lowerQuery) ||
      place.stateName.toLowerCase().includes(lowerQuery) ||
      place.description.toLowerCase().includes(lowerQuery)
  );
};
